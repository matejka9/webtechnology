	<section>
	<h3>Náš príbeh</h3>
	<p>Reštaurácia Tutorial 3 2016 je našou najnovšou  a už  štvrtou otvorenou pobočkou na Slovensku. Idea otvoriť si vlastnú reštauračnú sieť vznikla najmä kvôli našej rodinnej kulinárskej histórií a pýšime sa už tretou generáciou šéfkuchárov, ktorý precestovali a varili snáď  v každom kraji sveta.</p>
	<p>Práve tieto nazbierané skúsenosti  a kombinácie toho najlepšie zo sveta s čarom slovenskej kuchyne chceme ponúknuť aj Vám, bez toho aby ste museli opustiť hranice krajiny.</p>
	<p>Už piaty rok za sebou sme obhájili 4 hviezdičky a denne sa  tešíme z našej narastajúcej klientely a preto prichádzame ponúkať naše služby aj tu v centre Bratislavy.</p>
	<p> S viac ako 90 ročnou rodinnou skúsenosťou a štipkou soli v dlaniach  si Vás dovoľujeme pozvať k nám na nezabudnuteľný zážitok.</p>
	

	</section>

	<section>
	<h3> Tradícia a ocenenia </h3>
	<p>Vašim šéfkuchárom bude svetoznámy slovenský prodigy kulinár Dušan Zagy. Pokiaľ sa pohybujete vo svete varenia toto meno Vám určite zarezonovalo v pamäti. Pokiaľ nie tu je len niekoľko ocenení, ktorými sa pýšime: "Najlepšie kura", "Best teamwork", "Top Chef" , alebo "Varíme holými rukami".</p>
	<p>Okrem perfektnej kvality jedľa, najlepších kuchárov pod slnkom a profesionálneho týmu Vám ponúkame aj menu z ktorého si vyberie každý. Ponúkame Vám všetky druhy  mäsitých, či bezmäsitých jedál,  ryby, dezerty, polievky, alebo šaláty. Jednou z našich najviac oceňovaných stránok je pomer kvality jedľa ku cene a najmä rýchlosť našej obsluhy. Navštívte nás a presvedčte sa sami.</p>
