	<section>
	<h2>Náš príbeh</h2>
	<p>Reštaurácia Tutorial 3 2016 je našou najnovšou a v poradí už treťou otvorenou pobočkou na Slovensku. Nápad otvoriť si vlastnú reštauračnú sieť vznikla najmä kvôli našej rodinnej kulinárskej histórií. Pýšime sa už treťou generáciou šéfkuchárov, ktorí precestovali a varili na každom kontinente sveta.</p>
	<p>Práve tieto nazbierané skúsenosti, ako aj kombinácie toho najlepšieho zo sveta chceme ponúknuť aj Vám, bez toho aby ste museli opustiť hranice Slovenska.</p>
	<p>Už piaty rok za sebou sme obhájili 4 hviezdičky a denne sa tešíme z našej narastajúcej klientely. Z týchto dôvodov prichádzame ponúkať naše služby aj v centre Bratislavy.</p>
	<p>S viac ako 90 ročnou rodinnou skúsenosťou a štipkou soli v dlaniach si Vás dovoľujeme pozvať na nezabudnuteľný zážitok.</p>
	

	</section>

	<section>
	<h2>Tradícia a ocenenia</h2>
	<p>Vašim šéfkuchárom bude svetoznámy slovenský prodigy kulinár Dušan Zagy. Pokiaľ sa pohybujete vo svete varenia tak Vám toto meno Vám určite zarezonovalo v pamäti. Pokiaľ nie, tak tu je len niekoľko ocenení, ktorími sa pýšime práve vďaka našemu kuchárskemu tímu : "Najlepšie kura", "Best teamwork", "Top Chef" , alebo "Varíme holými rukami".</p>
	<p>Okrem perfektnej kvality jedla, najlepších kuchárov pod slnkom a profesionálneho tímu, Vám ponúkame aj menu, z ktorého si vyberie každý. Ponúkame Vám všetky druhy mäsitých, či bezmäsitých jedál, ako aj ryby, dezerty, polievky, alebo šaláty. Jednou z našich najviac oceňovaných vlastností je pomer kvality jedla k cene a najmä rýchlosť našej obsluhy. Navštívte nás a presvedčte sa sami!</p>
