	<p>Poloha reštaurácie sa nachádza priamo v centre mesta na ulici Nedbalová 10. Táto tichá lokácia je ideálna pre vychutnanie si svojho jedla a rekreáciu počas náročného pracovného dňa. V prípade, že následne plánujete turistiku po meste, neďaleko reštaurácie sa nachádza niekoľko významných historických pamiatok, ku ktorím sa dostanete peši do niekoľkých minút. Niektorými z nich sú Stará Tržnica, Primaciálny palác, Michalská veža, Dóm sv. Martina, alebo Slovenské národne divadlo.</p>
